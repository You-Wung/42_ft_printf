int	flag(int x)
{
	if (x == '-' || x == '0' || x == '.' || x == '*')
		return (1);
	return (0);
}

int	width(int x)
{
	if (x >= '1' && x <= '9')
		return (1);
	return (0);
}

int	number(int x)
{
	if (x >= '0' && x <= '9')
		return (1);
	return (0);
}
